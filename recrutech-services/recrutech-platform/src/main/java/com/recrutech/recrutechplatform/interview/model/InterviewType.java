package com.recrutech.recrutechplatform.interview.model;

/**
 * Enum representing the type of interview.
 * Defines the format/medium through which the interview will be conducted.
 */
public enum InterviewType {
    /**
     * Telephone interview
     */
    PHONE,
    
    /**
     * Video conference interview (e.g., Zoom, Teams, Meet)
     */
    VIDEO,
    
    /**
     * In-person interview at company location
     */
    ONSITE
}
